DROP TABLE SHIPPERS;
DROP TABLE ORDERS;
DROP TABLE SUPPLIERS;
DROP TABLE CATEGORIES;
DROP TABLE PRODUCTS;
DROP TABLE ORDER_DETAILS;

DROP SEQUENCE EMPLOYEE_ID;
DROP SEQUENCE ORDER_ID;
DROP SEQUENCE CATEGORY_ID;
DROP SEQUENCE PRODUCT_ID;
DROP SEQUENCE ORDER_DETAIL_ID;

CREATE SEQUENCE EMPLOYEE_ID
START WITH 1000
INCREMENT BY 1;
CREATE SEQUENCE ORDER_ID
START WITH 1000
INCREMENT BY 1;
CREATE SEQUENCE CATEGORY_ID
START WITH 1000
INCREMENT BY 1;
CREATE SEQUENCE PRODUCT_ID
START WITH 1000
INCREMENT BY 1;
CREATE SEQUENCE ORDER_DETAIL_ID
START WITH 1000
INCREMENT BY 1;

CREATE TABLE EMPLOYEES 
(
        EMPLOYEE_ID VARCHAR2(15) PRIMARY KEY,
        MANAGER VARCHAR2(15) NOT NULL,
        NAME  VARCHAR2(15) NOT NULL,
        BIRTH_DATE VARCHAR2(15) NOT NULL,
        PHOTO VARCHAR2(15),
        NOTES VARCHAR2(15)
);
CREATE TABLE CUSTOMERS
(

  CUSTOMER_ID    VARCHAR2(15) PRIMARY KEY,
  PASSWORD       VARCHAR2(15)NOT NULL,
  CUSTOMER_NAME  VARCHAR2(15)NOT NULL,
  SSN            VARCHAR2(15)NOT NULL,
  ADDRESS        VARCHAR2(15)NOT NULL,
  CITY           VARCHAR2(50)NOT NULL,
  POSTAL_CODE    VARCHAR2(15),
  PHOTO          VARCHAR2(15)DEFAULT 'default_photo',
  PHONE          VARCHAR2(15)
);
CREATE TABLE SHIPPERS
(
  SHIPPER_ID    VARCHAR2(15 BYTE)PRIMARY KEY,
  SHIPPER_NAME  VARCHAR2(15 BYTE)               NOT NULL,
  PHONE         VARCHAR2(15 BYTE)               NOT NULL
);
CREATE TABLE ORDERS
(
  ORDER_ID     NUMBER PRIMARY KEY,
  CUSTOMER_ID  VARCHAR2(15 BYTE)                NOT NULL,
  EMPLOYEE_ID  VARCHAR2(15 BYTE)                NOT NULL,
  ODER_DATE    DATE                             DEFAULT sysdate,
  SHIPPER_ID   VARCHAR2(15 BYTE)                NOT NULL
);
CREATE TABLE SUPPLIERS
(
  SUPPLIER_ID    VARCHAR2(15 BYTE)PRIMARY KEY,
  SUPPLIER_NAME  VARCHAR2(15 BYTE)              NOT NULL,
  CONTACT_NAME   VARCHAR2(15 BYTE)              NOT NULL,
  ADDRESS        VARCHAR2(15 BYTE)              NOT NULL,
  CITY           VARCHAR2(15 BYTE)              NOT NULL,
  POSTAL_CODE    VARCHAR2(15 BYTE)              NOT NULL,
  COUNTRY        VARCHAR2(15 BYTE)              NOT NULL,
  PHONE          VARCHAR2(15 BYTE)              NOT NULL
);
CREATE TABLE CATEGORIES
(
  CATEGORY_ID    NUMBER PRIMARY KEY,
  CATEGORY_NAME  VARCHAR2(15 BYTE)              NOT NULL,
  DESCRIPTION    VARCHAR2(15 BYTE)
);
CREATE TABLE PRODUCTS
(
  PRODUCT_ID    NUMBER PRIMARY KEY,
  PRODUCT_NAME  VARCHAR2(15 BYTE)               NOT NULL,
  SUPPLIER_ID   VARCHAR2(15 BYTE)               NOT NULL,
  CATEGORY_ID   NUMBER                          NOT NULL,
  UNIT          VARCHAR2(15 BYTE)               NOT NULL,
  PRICE         NUMBER                          NOT NULL
);
CREATE TABLE ORDER_DETAILS
(
  ORDER_DETAIL_ID  NUMBER PRIMARY KEY,
  ORDER_ID         NUMBER(15)                   NOT NULL,
  PRODUCT_ID       NUMBER(15)                   NOT NULL,
  QUANTITY         NUMBER                       DEFAULT 1
);

